package com.bytezone.diskbrowser.visicalc;

// -----------------------------------------------------------------------------------//
class Int extends ValueFunction
// -----------------------------------------------------------------------------------//
{
  // ---------------------------------------------------------------------------------//
  Int (Cell cell, String text)
  // ---------------------------------------------------------------------------------//
  {
    super (cell, text);
    assert text.startsWith ("@INT(") : text;
  }

  // ---------------------------------------------------------------------------------//
  @Override
  public double calculateValue ()
  // ---------------------------------------------------------------------------------//
  {
    return (int) source.getDouble ();
  }
}